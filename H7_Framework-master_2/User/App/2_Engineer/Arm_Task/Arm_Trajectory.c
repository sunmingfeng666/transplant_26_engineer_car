float Arm_Traj_TotalTime(Arm_Traj_e traj)
{
    if (traj >= ARM_TRAJ_COUNT) return 0.0f;
    return s_traj_meta[traj].total_time;
}

/*
 * 三次多项式分段插值。移植自旧框架 get_joint_position()，
 * 逻辑不变：按 current_t 定位分段，dt 乘 scale 后求多项式值。
 */
float Arm_Traj_GetJoint(Arm_Traj_e traj, uint8_t joint_idx0, float current_t)
{
    const Arm_Traj_Meta_t *m;
    int seg = 0;
    int coeff_offset;
    float dt, a, b, c, d;

    if (traj >= ARM_TRAJ_COUNT || joint_idx0 > 5U) return 0.0f;
    m = &s_traj_meta[traj];

    if (current_t <= 0.0f) current_t = 0.0f;
    if (current_t >= m->total_time) current_t = m->total_time - 0.0001f;

    for (int i = 0; i < m->segments; i++) {
        if (current_t >= m->t_knots[i] && current_t < m->t_knots[i + 1]) {
            seg = i;
            break;
        }
    }

    dt = (current_t - m->t_knots[seg]) * m->scale;
    coeff_offset = ((int)joint_idx0 * m->segments + seg) * 4;

    a = m->coeffs[coeff_offset + 0];
    b = m->coeffs[coeff_offset + 1];
    c = m->coeffs[coeff_offset + 2];
    d = m->coeffs[coeff_offset + 3];

    return ((a * dt + b) * dt + c) * dt + d;
}
